/** \brief  esta funcion suma 2 (dos) operandos.
 *
 * \param Operando numero1, variable del tipo float (flotante).
 * \param Operando numero2, variable del tipo float (flotante).
 * \return Valor del tipo float (flotante). El cual es la suma de los dos operando ingresados en la funcion.
 *
 */
float suma (float, float);



/** \brief  esta funcion resta 2 (dos) operandos.
 *
 * \param Operando numero1, variable del tipo float (flotante).
 * \param Operando numero2, variable del tipo float (flotante).
 * \return Valor del tipo float (flotante). El cual es la resta del operando numero1 con el operando numero2.
 *
 */
float resta (float, float);



/** \brief  esta funcion multiplica 2 (dos) operandos.
 *
 * \param Operando numero1, variable del tipo float (flotante).
 * \param Operando numero2, variable del tipo float (flotante).
 * \return Valor del tipo float (flotante). El cual es el producto del operando numero1 con el operando numero2.
 *
 */
float multiplicacion (float, float);



/** \brief  esta funcion divide 2 (dos) operandos.
 *
 * \param Operando numero1, variable del tipo float (flotante).
 * \param Operando numero2, variable del tipo float (flotante).
 * \return Valor del tipo float (flotante). El cual es el cociente entre operando numero1(dividendo) con el operando numero2(divisor).
 *
 */
float division (float, float);




/** \brief  esta funcion calcula el factorial de un operando.
 *
 * \param Operando numero1, variable del tipo int (entero). Numeros naturales positivos (no incluido el cero).
 * \return Valor del tipo int (entero). Se obtiene el factorial del operando numero1 ingresado. Es el producto de todos los numeros naturales empezando por 1(uno) hasta el operando numero1 ingresado.
 *
 */
int factorial(int);